     1、角色：<br />
　　　狼人（3人），女巫（1人），预言家（1人），守卫（1人），平民（2人），一共8人。<br />
　　　2、角色技能：<br/>
　　　狼人——通过3个人的商定，选定共同要杀的人；<br />
　　　女巫——拥有毒药、解药各一瓶，毒药可以毒死任何人，解药可以解救被狼人杀死的人；<br />
　　　预言家——可以问系统其余任何一个人的身份；<br />
　　　守卫——可以保护任何一个人不被狼人杀死，相邻两轮不能保护同一个人；<br />
　　　平民——没有任何技能。<br />
　　　3、游戏步骤：<br />
　　　1）发身份卡给8个玩家；<br />
　　　2）系统发出"天黑请闭眼"指令；<br />
　　　3）系统发出"狼人出来对个眼"，3个狼人互相认识一下；<br />
　　　4）系统发出"天亮了，竞选村长"，任何人可以选择竞选村长，村长的目标是带领大家消灭狼人（狼人可以假扮村长糊弄人）；<br />
　　　5）系统发出"天黑请闭眼"指令；<br />
　　　6）系统发出"守卫出来"，守卫保护一个人；<br />
　　　7）系统发出"狼人出来杀人"，狼人通过商议决定杀一个人；<br />
　　　8）系统发出"女巫出来"，女巫选择救人或者毒死人；<br />
　　　9）系统发出"预言家出来"，预言家向系统申请预言一个人的身份，系统回答是否是狼人；<br />
　　　10）系统发出"天亮了，进行发言"，判断是否有人被狼人杀害，然后8一人一句阐释身份与判断狼人的话；<br />
　　　11）村长做最后判断，投票投出最有可能是狼人者，使之出局；<br />
　　　12）一直进行，最后狼人数量与村名数量一样或多于，狼人赢；狼人都被清除，村民赢；<br />
　　　13）游戏结束。<br />
